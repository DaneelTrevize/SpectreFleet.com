				<h2>Policies</h2>
				
				<h3>NPSI</h3>
				<p>
					Within Eve Online, Spectre Fleet operates under "Not Purple? Shoot It!" (NPSI) rules of engagement. This means that anyone in your fleet (by default appearing Purple on your overview & brackets) is your friend, and everything else is a valid target.<br>
					The obvious exceptions to this are: during fleet form-up; when a fleet concludes; and if someone disconnects from the game causing them to drop from fleet. Please try to avoid initiating hostilities with isolated non-fleetmates travelling to or from the main fleet while they are in a ship that obviously matches the current fleet's composition and while they are also not obviously affiliated with any known nearby enemy fleets.<br>
					During our fleets, you are still welcome to work with other groups under temporary "Blue" positive standings, however this is usually only done for High Value Targets, large events or arranged fights. If you encounter someone that's personally Blue to you while in Spectre Fleet, you do not have to engage them, however the rest of your fleet will still be free to engage regardless of whether you're just a fleet member or the Fleet Commander.
				</p>
				
				<h3>Ships, not Structures</h3>
				<p>
					Spectre Fleet endeavours to provide fleet-on-fleet fights, thus targets such as starbases, TCUs, IHUBs, Jump Bridges, etc are not engaged as we feel there are better ways of provoking a fight than attacking structures or sovereignty.<br>
					With the introduction of Citadels and their different mechanics & nature, we have added an exception to this rule. FCs may use our fleets to take the opportunity to push a final timer for a Citadel, but still should not initiate reinforcing such structures using a Spectre fleet.
				</p>
				
				<h3>Feedback</h3>
				<p>
					Spectre Fleet self-polices our community via an <a href="/feedback">online feedback submission system</a>. This is anonymous and available for everyone to use, regardless of website registration, in-game fleet participation or Eve account status.<br>
					All Operator/Administrator rights (Muting, Banning/Kicking, etc) to our in-game channels and external communication resources should be tied to the Eve character names of our registered FCs, and all feedback is associated with a specified FC name. If you feel you have any constructive feedback, positive or negative, regarding your experience with Spectre Fleet and any specific FC's actions, please don't hesistate to submit it online. If you are unsure which specific FC may be responsible for the experience you wish to report, you can ask in our main in-game channel, or on our Discord, or address the feedback to any current member of Staff.
				</p>
				
				<h3>Etiquette</h3>
				<h4>In-game Channel & Advertising</h4>
				<p>
					The main "SF Spectre Fleet" in-game chat channel is our primary means of communicating with all players, and especially those new to Spectre. By default, all players are welcome here.<br>
					Please try to keep the discussion topics to those directly related to our fleets & activities, rather than arbitrary balance discussion or IRL politics while anyone is asking for help w.r.t. participating in our fleets. If you feel someone is abusing the channel, please try contacting our staff either in-game or via our Discord. We hand out temporary and permanent mutes & bans as Staff deems appropriate.<br>
					Other NPSI communities are not completely banned from advertising their activities in our channel, but they are asked to contact Staff first to receive approval before doing so, and so that we may negotiate cross-community advertising.<br>
					All event entries (Scheduled fleets, Active fleets, etc) in the channel MOTD should have the responsible Spectre FC listed against them. This ensures the community can easily provide feedback via the website for every event we participate in or promote. 3rd party events or unregistered FCs must still have the vouching registered Spectre FC's name listed against them.
				</p>
				<h4>Fleet, Mumble and Local chat</h4>
				<p>
					Fleets depend upon Mumble as well as the in-game Fleet chat channel for communications, and will interact with hostile pilots via the in-game Local chat channel, with the FC having the final say upon what topics are up for discussion. Anyone being rude or disruptive will be warned. Warnings may be followed by a muting or temporary kicking from such services, and/or from fleet.<br>
					A kicking from fleet will be with a reason given and a 10 second grace period following, after which the kicked person is no longer considered Purple for the remainder of that fleet or until the FC states otherwise during it. Anyone kicked is advised to leave the fleet's grid and system if they wish to retain their ship and pod, and reflect upon the reason given before returning to future fleets.
				</p>
				
				<h3>Ship Replacement Program</h3>
				<p>
					By default, Spectre does not offer an SRP, Ship Provision Program, or other direct assistance for pilots wishing to participate in our fleets. We do however permit FCs to offer such services on a per-fleet basis, at their discretion. This is often only offered to high-priority-target support ships such as those providing Interdiction Bubbles, Command Bursts, MJFGs, and Logistics.<br>
					We are also currently affiliated with a Azbel Citadel in the Dital solar system (where a significant percentage of our fleets currently depart from), where you may find favourable prices & stocks of items commonly used in our fleets.
				</p>
				
				<h3>Loot</h3>
				<p>
					By default, all loot generated during a Spectre Fleet worth above 25 million ISK should be handed to the FC.<br>
					It is then the FC's choice whether they share the value amongst all of the fleet, reward specific pilots for their efforts, use any sales to fund SRP, or do something else with it. Once again, any constructive feedback regarding any FC's actions should be submitted via <a href="/feedback">the online anonymous feedback form</a>.
				</p>

