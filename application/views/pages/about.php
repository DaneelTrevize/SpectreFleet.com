				<p class="lead">
					Spectre Fleet is an Online Gaming Community that was founded in 2013. What started as a small group of friends who had met through Eve Online, has since become a large community with over 8000 active members from every major timezone. As a community we focus on the social aspect of gaming, providing a place where gamers can hang out and play together, with no obligations. 
				</p>
				
				<p>
					Spectre Fleet was originally created as a completely open community, and our founding principles remain the same to this day. Everyone is welcome to join the Spectre Fleet community, with no obligations or commitments necessary. We maintain a presence in multiple games, mainly Eve Online, but you're more than welcome to join us just to hangout if that's what you want to do.
				</p>

				<p>
					Our community is managed by a team of over 150 volunteers who do everything from organize our daily events, to instruct new players on various topics. The unsung heroes of our community, these people are the ones who keep everything running. If you have any questions about Spectre Fleet, or if you would like to help give back to the community, talk to a member of the command team on our Mumble or Discord.
