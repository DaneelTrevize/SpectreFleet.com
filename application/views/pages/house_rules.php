				<h2>House Rules</h2>
				
				<p class="lead">
					This document is intended as a short easily readable summary of the Spectre house rules.
				</p>
				
				<h2>Rules</h2>
				
				<h3>General</h3>
				<p>
					<ul>
						<li>No sexist/racist/abusive comments, imagery. Zero tolerance policy instant ban.</li>
						<li>The listed FC for the fleet is the FC that leads the fleet, no other FC is allowed to overrule the listed FC for the fleet without his permission.</li>
					</ul>
				</p>
				
				<h3>Channel related</h3>
				<p>
					<ul>
						<li>No trading in channel without permission of Staff.</li>
						<li>No channel baiting, this is when you post a fake killmail linking to a channel.</li>
						<li>Do not spam.</li>
						<li>No recruiting in the Spectre channels.</li>
					</ul>
				</p>
				
				<h3>Voice comms related</h3>
				<p>
					<ul>
						<li>Use Push to talk, if you hot-mic, you will get muted.</li>
						<li>If you are in a fleet, you are on the Spectre Fleet Mumble and able to listen to the FC.</li>
						<li>All fleets hosted through Spectre use the Spectre Fleet Mumble.</li>
					</ul>
				</p>
				
				<h3>Fleets</h3>
				<p>
					<ul>
						<li>Do not shoot fleet members or disconnects. You will reimburse their ships! (some expections to them asking you to shoot them.)</li>
						<li>All loot above 25 million a piece gets dealt with by the FC. Take it with you, give it to the FC later.</li>
						<li>Do NOT use smartbombs during fleets without explicitly being allowed to by the FC.</li>
					</ul>
				</p>
				
				<h3>If you get banned here is what you can do</h3>
				<p>
					<ul>
						<li>Talk to a Staff member. They might review your case.</li>
						<li>If your corp or alliance got banned, talk to a Staff member. They will review the case, in general Spectre will never ban an entire corp or alliance.</li>
					</ul>
				</p>

