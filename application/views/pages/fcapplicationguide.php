				<p class="lead">
					In Spectre Fleet, a good intake of Junior FC's is key to the operation and consistent frequency of fleets.<br>
                    In order to introduce new FCs, they need a short explanation of the workings of Spectre as well as needing the proper roles and rights to FC for Spectre.<br>
                    This guide will give you, as a Staff member or Senior FC, the explanation of what to do and how to do it. You should be able to use this as a checklist to make sure the new FC is set up properly.<br>
					<br>
                    This guide is a quick fix until the intake process can be fully automated and tied to the "Accept as FC" button.
				</p>
				
                <h3>Preparation and things to do beforehand</h3>

                <p>
                    Tell the FC applicant to be ingame, on Discord, logged into the website as the character they are intending to use as their Spectre FC one, and logged in on Mumble.<br>
                    Postpone hitting the accept button until after the interview, because you still need to decide if you actually want to accept them yet. The accept button makes them a Junior FC if an account exists on the website with the applied character name. If that character isn't registered, an account is created for them as a Junior FC, and they can use the SSO-based Spectre Password reset feature to finish setting it up.
                </p>

                <h3>The interview part</h3>

                <p>
                    While we have no set questions to ask the applicant FC, it is important to get them talking in order to get a feel for them as a person and as an FC. <br>
                    There are no right and wrong answers and it is completely up to the discretion of the Senior FC or Staff member whether or not to accept the FC or not.
					<ul>
						<li>Why do you want to be a Spectre FC?</li>
						<li>What kind of fleets would you like to run?</li>
						<li>What kind of fleet sizes are you comfortable with being responsible for?</li>
						<li>What timeslot and fleet frequency would you normally run at? (to get a general idea when I would expect to see him/her/it online)</li>
					</ul>
                </p>

                <h3>Walkthrough and Spectre rules</h3>

                <p>
                    Walk them though the MOTD guide and the FC guide (tell them to not fuck up the channel MOTD)<br>
                    Tell them there is a 125 mil ISK/ship rough max limit for Junior FC fits<br>
                    Walk them through the website fits and doctrines features<br>
                    Tell them to not use SpectreFleet to bash structures unless its a short shitadel timer or there is likely a fight.<br>
                    no personal army stuff<br>
                    no sexist/racist stuff<br>
                    no shooting fleet members (kick them first)<br>
                    They control the fleet and they can tell other FC's in the fleet to not backseat them in the fleet.
                </p>

                <h3>Setting up rights and roles</h3>

                <p>
                    Then the hard part. If you accept the FC applicant, do these things:
					<ol>
						<li>Make them register their account on Mumble (right click their own name and register user)<br>
                    Tell them how to backup their certificates, so you don't have to redo this step in the future. The Spectre Mumble guide details how to do so at the bottom.<br>
                    Right click the Spectre fleet main Mumble thingy and go to "Edit" --> "groups" --> "fc" --> select name from the dropdown and press "Add"</li>
						<li>add them as Operators to xup1 and xup2 ingame, add them to "Spectre fleet" if you think they are trustworthy enough, otherwise do it after their first succesful fleet.</li>
						<li>invite them to "Spectre Command" channel ingame</li>
						<li>add them on Discord to "#command". Do this by going "Spectre fleet" in the top right of Discord --> "Server settings" --> "members" --> find the git you are looking for --> "+" Select "FC"</li>
						<li>Tell people in Discord who you have accept and what timezone, make the Junior FC write some silly introduction.</li>
					</ol>
                </p>

